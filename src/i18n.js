let currentLang = localStorage.getItem('mori-lang') || 'en';

function applyTranslations(lang) {
  currentLang = lang;
  localStorage.setItem('mori-lang', lang);
  document.documentElement.lang = lang;

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    const val = translations[lang]?.[key];
    if (val !== undefined) {
      el.textContent = val;
    }
  });

  document.querySelectorAll('[data-i18n-html]').forEach((el) => {
    const key = el.getAttribute('data-i18n-html');
    const val = translations[lang]?.[key];
    if (val !== undefined) {
      el.innerHTML = val;
    }
  });

  const toggleBtn = document.getElementById('lang-toggle');
  if (toggleBtn) {
    toggleBtn.querySelector('.lang-toggle__active').textContent = lang === 'en' ? 'EN' : 'VI';
    toggleBtn.querySelector('.lang-toggle__inactive').textContent = lang === 'en' ? 'VI' : 'EN';
  }
}

function toggleLang() {
  applyTranslations(currentLang === 'en' ? 'vi' : 'en');
}

function getCurrentLang() {
  return currentLang;
}

function getNavLabel(sectionId) {
  const map = {
    hero: 'nav.home',
    about: 'nav.about',
    classes: 'nav.classes',
    studio: 'nav.studio',
    offerings: 'nav.offerings',
    stories: 'nav.stories',
    pricing: 'nav.pricing',
    booking: 'nav.book',
  };
  const key = map[sectionId];
  return key ? (translations[currentLang]?.[key] || sectionId.toUpperCase()) : sectionId.toUpperCase();
}

export { applyTranslations, toggleLang, getCurrentLang, getNavLabel };
